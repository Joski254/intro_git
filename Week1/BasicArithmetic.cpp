#include <iostream>

int main() {
    
    int num1 = 10, num2 = 3;

    std::cout << num1 << " + " << num2 << " = " << (num1 + num2) << std::endl;

    std::cout << num1 << " - " << num2 << " = " << (num1 - num2) << std::endl;

    std::cout << num1 << " * " << num2 << " = " << (num1 * num2) << std::endl;

    std::cout << num1 << " / " << num2 << " = " << (num1 / num2) << std::endl;

    std::cout << num1 << " % " << num2 << " = " << (num1 % num2) << std::endl;

    return 0;
}
